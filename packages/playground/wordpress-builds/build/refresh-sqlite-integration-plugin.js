import yargs from 'yargs';
import { promises as fs, statSync } from 'fs';

// The latest version of the SQLite plugin is the develop branch.
const latestVersion = 'develop';

const parser = yargs(process.argv.slice(2))
	.usage('Usage: $0 [options]')
	.options({
		['output-dir']: {
			type: 'string',
			description: 'output directory',
			required: true,
		},
		['plugin-version']: {
			type: 'string',
			description: 'version',
			required: false,
			default: latestVersion,
		},
	});

const args = parser.argv;

// Get version and URL.
const version = args.pluginVersion;
const url = 'develop' === version
	? `https://github.com/Automattic/sqlite-database-integration/archive/refs/heads/${version}.zip`
	: `https://github.com/Automattic/sqlite-database-integration/archive/refs/tags/${version}.zip`;

// Load versions map.
const versionsPath = `${args.outputDir}/sqlite-database-integration-versions.json`;
let versions = {};
try {
	const data = await fs.readFile(versionsPath, 'utf8');
	versions = JSON.parse(data);
} catch (e) {
	versions = {};
}

// Download the plugin.
const outputZipPath = `${args.outputDir}/sqlite-database-integration-${version}.zip`;
const sqliteResponse = await fetch(url);
if (!sqliteResponse.ok) {
	throw new Error(`Failed to download SQLite integration plugin: ${sqliteResponse.statusText}`);
}
const sqliteZip = Buffer.from(await sqliteResponse.arrayBuffer());
await fs.writeFile(outputZipPath, sqliteZip);

// Update versions map.
versions[version] = version;
await fs.writeFile(versionsPath, JSON.stringify(versions, null, 2));

// Load sizes map.
const sizes = {};
for (const version of Object.keys(versions)) {
	const zipPath = `${args.outputDir}/sqlite-database-integration-${version}.zip`;
	try {
		sizes[version] = statSync(zipPath).size;
	} catch (e) {
		sizes[version] = 0;
	}
}

// Refresh get-sqlite-driver-module-details.ts
const slugify = (v) => v.replace(/[^a-zA-Z0-9_]/g, '_');
const getSqliteDriverModuleDetailsPath = `${args.outputDir}/get-sqlite-driver-module-details.ts`;
const getSqliteDriverModuleDetailsContent =
`${Object.keys(versions)
	.map(
		(version) =>
			`// @ts-ignore
import url_${slugify(version)} from './sqlite-database-integration-${version}.zip?url';`
	)
	.join('\n')}

/**
 * This file was auto generated by:
 *   packages/playground/wordpress-builds/build/refresh-sqlite-integration-plugin.js
 * DO NOT CHANGE MANUALLY!
 * This file must statically exists in the project because of the way
 * vite resolves imports.
 */
export const LatestSqliteDriverVersion = '${latestVersion}';

export function getSqliteDriverModuleDetails(
	version = LatestSqliteDriverVersion
): {
	size: number;
	url: string;
} {
	switch (version) {
		${Object.keys(versions)
			.map(
				(version) =>`
		case '${version}':
			/** @ts-ignore */
			return {
				size: ${JSON.stringify(sizes[version])},
				url: url_${slugify(version)},
			};`).join('')}
	}
	throw new Error(
		'Unsupported SQLite integration plugin version: ' + version
	);
}
`;
await fs.writeFile(getSqliteDriverModuleDetailsPath, getSqliteDriverModuleDetailsContent);
